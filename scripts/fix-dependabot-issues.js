#!/usr/bin/env node

/**
 * Script to automatically check for Dependabot alerts and fix dependency issues
 * Usage: node scripts/fix-dependabot-issues.js
 */

const { execSync } = require('child_process')
const fs = require('fs')
const path = require('path')

// Colors for console output
const colors = {
  reset: '\x1b[0m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  red: '\x1b[31m',
  cyan: '\x1b[36m',
}

function log(message, color = 'reset') {
  console.log(`${colors[color]}${message}${colors.reset}`)
}

function execCommand(command, options = {}) {
  try {
    const result = execSync(command, {
      encoding: 'utf-8',
      stdio: options.silent ? 'pipe' : 'inherit',
      ...options,
    })
    return { success: true, output: result }
  } catch (error) {
    return {
      success: false,
      error: error.message,
      output: error.stdout || error.stderr,
    }
  }
}

async function fetchDependabotAlerts() {
  log('\n🔍 Checking for Dependabot alerts...', 'cyan')

  // Check if gh CLI is installed
  const ghCheck = execCommand('which gh', { silent: true })
  if (!ghCheck.success) {
    log(
      '⚠️  GitHub CLI (gh) not installed. Install it with: brew install gh',
      'yellow',
    )
    log('Skipping Dependabot alert check...', 'yellow')
    return []
  }

  // Fetch Dependabot alerts using GitHub CLI
  const alertsCommand =
    'gh api repos/{owner}/{repo}/dependabot/alerts --paginate'
  const result = execCommand(alertsCommand, { silent: true })

  if (result.success && result.output) {
    try {
      const alerts = JSON.parse(result.output)
      log(
        `Found ${alerts.length} Dependabot alert(s)`,
        alerts.length > 0 ? 'yellow' : 'green',
      )

      if (alerts.length > 0) {
        alerts.forEach((alert, index) => {
          log(`\n${index + 1}. ${alert.security_advisory.summary}`, 'yellow')
          log(`   Package: ${alert.dependency.package.name}`, 'reset')
          log(`   Severity: ${alert.security_advisory.severity}`, 'reset')
          log(`   Current: ${alert.dependency.manifest_path}`, 'reset')
        })
      }

      return alerts
    } catch (e) {
      log('Could not parse Dependabot alerts', 'yellow')
      return []
    }
  }

  return []
}

function runPnpmAudit() {
  log('\n🔍 Running pnpm audit...', 'cyan')

  const auditResult = execCommand('pnpm audit', { silent: true })

  if (auditResult.output) {
    console.log(auditResult.output)
  }

  return auditResult
}

function runPnpmAuditFix() {
  log('\n🔧 Running pnpm audit --fix...', 'cyan')

  const fixResult = execCommand('pnpm audit --fix')

  if (fixResult.success) {
    log('✅ Successfully ran pnpm audit --fix', 'green')
  } else {
    log('⚠️  Some issues could not be automatically fixed', 'yellow')
  }

  return fixResult
}

function updateDependencies() {
  log('\n📦 Updating dependencies with overrides...', 'cyan')

  const installResult = execCommand('pnpm install')

  if (installResult.success) {
    log('✅ Dependencies updated successfully', 'green')
  } else {
    log('❌ Failed to update dependencies', 'red')
  }

  return installResult
}

function checkForOutdatedPackages() {
  log('\n📊 Checking for outdated packages...', 'cyan')

  const outdatedResult = execCommand('pnpm outdated', { silent: true })

  if (outdatedResult.output) {
    console.log(outdatedResult.output)
  }
}

function generateReport() {
  const reportPath = path.join(process.cwd(), 'dependency-fix-report.md')
  const timestamp = new Date().toISOString()

  const report = `# Dependency Fix Report
Generated: ${timestamp}

## Actions Taken
1. Checked for Dependabot alerts
2. Ran pnpm audit
3. Applied pnpm audit --fix
4. Updated dependencies with pnpm install
5. Checked for remaining outdated packages

## Next Steps
- Review the changes in package.json and pnpm-lock.yaml
- Test the application to ensure everything works
- Commit the changes if satisfied
- Consider adding manual overrides in package.json for specific versions

---
Generated by fix-dependabot-issues.js
`

  fs.writeFileSync(reportPath, report, 'utf-8')
  log(`\n📄 Report saved to: ${reportPath}`, 'green')
}

async function main() {
  log('🚀 Starting Dependabot Issue Fixer', 'cyan')
  log('='.repeat(50), 'cyan')

  try {
    // Step 1: Fetch Dependabot alerts
    const alerts = await fetchDependabotAlerts()

    // Step 2: Run pnpm audit
    runPnpmAudit()

    // Step 3: Run pnpm audit --fix
    runPnpmAuditFix()

    // Step 4: Update dependencies
    updateDependencies()

    // Step 5: Check for outdated packages
    checkForOutdatedPackages()

    // Step 6: Generate report
    generateReport()

    log('\n✨ Process completed!', 'green')
    log('\nRecommendations:', 'cyan')
    log('1. Review the changes made to package.json and pnpm-lock.yaml')
    log('2. Run your tests: pnpm test')
    log('3. Check the application: pnpm dev')
    log(
      '4. Commit changes: git add . && git commit -m "fix: resolve dependency security issues"',
    )
  } catch (error) {
    log(`\n❌ Error: ${error.message}`, 'red')
    process.exit(1)
  }
}

main()
